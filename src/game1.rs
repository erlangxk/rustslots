macro_rules! linedef1 {
    (
        $([$($x:expr),+]),+
    ) => {
            [$(
                [$(($x,0)),+]
            ),*]
    };
}

pub static LINES: [[(u8, u8); 3]; 8] = linedef1![
    [3, 4, 5],
    [0, 1, 2],
    [6, 7, 8],
    [0, 3, 6],
    [1, 4, 7],
    [2, 5, 8],
    [0, 4, 8],
    [2, 4, 6]
];
